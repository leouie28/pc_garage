    {
        $image = new Image;
         if($request->hasfile('filename'))
        {
        $filePath = Storage::disk('public')->putFile('', $request->file('filename'));
        $image->filename = $filePath;
        }
        $image->product_id = $request->product_id;
        $image->save();
        
        return $filePath;
    }

